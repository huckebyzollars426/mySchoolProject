import math

def calculate_square_root(num):
    """
    Calculate and return the square root of the given number.
    
    Args:
    num (float or int): The number to calculate the square root of.
    
    Returns:
    float: The square root of the given number.
    """
    return math.sqrt(num)

# Example usage
result = calculate_square_root(16)
print(result)  # Output will be approximately 4.0
